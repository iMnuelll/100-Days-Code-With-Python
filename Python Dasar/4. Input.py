# Data String
data_string =  input("Massukkan nama : ")
print("data = ", data_string, ",type = ",type(data_string))

# Data Integer
data_integer = int(input("Masukkan angka : "))
print("data = ", data_integer, ",type = ",type(data_integer)) 

# Data Float
data_float = float(input("Masukkan angka desimal : "))
print("data = ", data_float, ",type = ",type(data_float))

# Data Boolean
data_bool = bool(int(input("Masukkan nilai boolean : ")))
print("data = ", data_bool, ",type = ",type(data_bool))